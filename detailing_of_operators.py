class solution(object):
  def detailing():
    f = int(input())
    s = int(input())
    
    print(f"{f} < {s} is {f < s}")
    print(f"{f} <= {s} is {f <= s}")
    print(f"{f} > {s} is {f > s}")
    print(f"{f} >= {s} is {f >= s}")
    print(f"{f} == {s} is {f == s}")
    print(f"{f} != {s} is {f != s}")

obj=solution()
obj.detailing()
